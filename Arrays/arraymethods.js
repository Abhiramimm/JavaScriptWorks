// array methods

/*

    class Array{

    push(object){}
    pop()
    unshift() add object to the beginning
    shift()  remove object from the beginning
    includes(object)

    }

*/


colors=["red","green","blue"]
console.log(colors);

//push()
colors.push("yellow")
console.log(colors);

//pop()
var popped_obj=colors.pop()
console.log(colors);

//unshift()
colors.unshift("black")
console.log(colors);

//shift()
colors.shift()
console.log(colors);

//includes(object)
console.log(colors.includes("red"));


